#include "../push_swap.h"

void	pb(t_stk *a, t_stk *b)
{
	int	temp;

	if (a->top == -1)
		return ;
	temp = a->data[a->top];
	a->data[a->top] = 0;
	a->top--;
	b->top++;
	b->data[b->top] = temp;
	ft_printf("%s", "pb\n");
}
