#include "../push_swap.h"

void	rb(t_stk *b)
{
	int	i;
	int	temp;

	if (b->top > 0)
	{
		i = 0;
		temp = b->data[0];
		while (i < b->top)
		{
			b->data[i] = b->data[i + 1];
			i++;
		}
		b->data[i] = temp;
		ft_printf("%s", "rb\n");
	}
}
