#include "../push_swap.h"

void	rrb(t_stk *b)
{
	int	temp;
	int	i;

	i = b->top;
	temp = b->data[b->top];
	while (i > 0)
	{
		b->data[i] = b->data[i - 1];
		i--;
	}
	b->data[0] = temp;
	ft_printf("%s", "rrb\n");
}
